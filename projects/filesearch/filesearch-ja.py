# 指定したフォルダー(最大2レベルまでのサブフォルダーを含む)を検索し、そこに
# 含まれるすべての`.jpg`ファイルのリストをまとめるスクリプトを記述してください。
# このリストには各`.jpg`ファイルの完全なパスが含まれるようにします。
# 
# 以下の項目のトレーニングが必要です。
# 
# - `for`ループの使用
# - 条件文の使用
# - `pathlib`の操作
# - 入れ子構造のループの使用
# 
# さらに踏み込んでやってみたい場合は、フォルダー内の各ファイル拡張子が
# 含まれるリストを作成してみてください。
# 小さなフォルダーから始めると、プログラムが正しく動作しているかどうかの確認が
# 簡単になります。その後、より大きなフォルダーを検索するようにしましょう。
# このプログラムは、コンピュータにある、指定した任意のフォルダーに対して動作します。
